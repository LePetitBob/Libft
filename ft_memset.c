#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*res;

	i = 0;
	res = (char *)s;
	while (i < n)
	{
		res[i] = c;
		i++;
	}
	return ((void*)s);
}
